<?php
require 'db.php'; // подключаем файл для соединения с базой данных

// Проверяем, передан ли id персонажа для удаления
if (isset($_GET['id'])) {
    $id = $_GET['id'];

    // Подготавливаем запрос на удаление персонажа
    $stmt = $pdo->prepare("DELETE FROM characters WHERE id = ?");
    $stmt->execute([$id]);

    // Перенаправляем на страницу персонажей после удаления
    header("Location: characters.php");
    exit; // Прерываем выполнение скрипта
} else {
    // Если id не передан, выводим ошибку
    echo "Персонаж не найден.";
}
?>
